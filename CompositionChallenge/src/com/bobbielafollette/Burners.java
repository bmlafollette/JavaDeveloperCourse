public class Burners {

    private int smallBurners;
    private int largeBurners;

    public Burners(int smallBurners, int largeBurners) {
        this.smallBurners = smallBurners;
        this.largeBurners = largeBurners;
    }

    public int getSmallBurners() {
        return smallBurners;
    }

    public int getLargeBurners() {
        return largeBurners;
    }
}
